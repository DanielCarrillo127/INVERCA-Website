// Devuelve los municipios de un departamento (ordenados) o [] si no existe.
export const municipiosDe = (departamento) =>
  (colombia[departamento] || [])
    .filter((m, i, arr) => arr.indexOf(m) === i)
    .sort((a, b) => a.localeCompare(b, "es"));
